import { createContext, useContext } from "react";

export type UiLanguage = "de" | "en";

export const UI_LANGUAGE_STORAGE_KEY = "tafforge.ui.language.v1";

export const UI_LANGUAGE_OPTIONS: Array<{ value: UiLanguage; label: string }> = [
  { value: "de", label: "Deutsch" },
  { value: "en", label: "English" },
];

export const readStoredUiLanguage = (): UiLanguage => {
  if (typeof window === "undefined") {
    return "de";
  }
  const stored = window.localStorage.getItem(UI_LANGUAGE_STORAGE_KEY);
  return stored === "en" ? "en" : "de";
};

export const storeUiLanguage = (language: UiLanguage): void => {
  if (typeof window !== "undefined") {
    window.localStorage.setItem(UI_LANGUAGE_STORAGE_KEY, language);
  }
};

