export type UiText = (typeof uiText)[UiLanguage];

export interface UiI18nContextValue {
  language: UiLanguage;
  setLanguage: (language: UiLanguage) => void;
  text: UiText;
  locale: string;
}

export const UiI18nContext = createContext<UiI18nContextValue | null>(null);

export const useUiI18n = (): UiI18nContextValue => {
  const ctx = useContext(UiI18nContext);
  if (!ctx) {
    throw new Error("UiI18nContext missing");
  }
  return ctx;
};
